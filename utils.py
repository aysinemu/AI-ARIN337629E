# Characters that are easily confused with each other on license plates
CONFUSABLE_PAIRS = {
    'O': '0', '0': 'O',
    'I': '1', '1': 'I',
    'B': '8', '8': 'B',
    'D': '0',  # D looks like 0
    'S': '5', '5': 'S',
    'Z': '2', '2': 'Z',
    'G': '6', '6': 'G',
    'Q': '0',
    'L': '1',
    'T': '7', '7': 'T',
}

# Higher penalty weights for confusable character mismatches
CONFUSABLE_WEIGHT = 2.0  # 2x penalty for confusable char errors
NORMAL_WEIGHT = 1.0


def get_char_weights(gt_text: str, pred_text: str, num_chars: int = 7) -> List[float]:
    """
    Computes per-character loss weights based on OCR confusability heuristics.
    
    This function analyzes the predicted string against the ground truth. If a character
    is incorrectly predicted AND the mistake involves known confusable pairs (e.g., '0' vs 'O', '8' vs 'B'),
    the weight for that specific character position is exponentially increased to heavily penalize the model.
    This forces the Super-Resolution mechanism to synthesize these ambiguous characters much more cleanly.
    
    Args:
        gt_text (str): Ground truth alphanumeric plate text (e.g., "AVL5215").
        pred_text (str): Predicted alphanumeric plate text generated by the OCR module.
        num_chars (int, optional): The normalized length of the license plate. Defaults to 7.
    
    Returns:
        List[float]: A list of float weights, exactly `num_chars` elements long, to be multiplied by the baseline CE loss.
    """
    weights: List[float] = []
    min_len = min(len(gt_text), len(pred_text), num_chars)
    
    for i in range(num_chars):
        if i >= min_len:
            weights.append(CONFUSABLE_WEIGHT)  # Missing/extra chars get high weight
            continue
            
        gt_c = gt_text[i].upper()
        pr_c = pred_text[i].upper()
        
        if gt_c == pr_c:
            weights.append(NORMAL_WEIGHT)
        elif gt_c in CONFUSABLE_PAIRS and CONFUSABLE_PAIRS[gt_c] == pr_c:
            weights.append(CONFUSABLE_WEIGHT)
        elif pr_c in CONFUSABLE_PAIRS and CONFUSABLE_PAIRS[pr_c] == gt_c:
            weights.append(CONFUSABLE_WEIGHT)
        else:
            weights.append(NORMAL_WEIGHT)
    
    return weights


