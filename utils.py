def save_training_state(state: Dict[str, Any], path: Union[Path, str], filename: str) -> None:
    """
    Serializes and saves the complete training environment.
    
    Includes model weights, optimizer state, epoch count, and training history.
    
    Args:
        state (Dict[str, Any]): Dictionary of objects to preserve.
        path (Union[Path, str]): Directory to save.
        filename (str): Target filename (e.g., 'bestmodel.pt').
    """
    path = Path(path)
    path.mkdir(parents=True, exist_ok=True)
    torch.save(state, path / filename)


def load_training_state(path: Union[Path, str]) -> Dict[str, Any]:
    """
    Restores the training environment from a saved checkpoint.
    
    Args:
        path (Union[Path, str]): Path to the `.pt` file.
        
    Returns:
        Dict[str, Any]: The loaded state dictionary.
    """
    return torch.load(path, map_location='cpu', weights_only=False) # type: ignore

