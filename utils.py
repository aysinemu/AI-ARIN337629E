def plot_losses(train_losses: List[float], val_losses: List[float], 
                save_path: Union[Path, str], title: str = 'Training Progress') -> None:
    """
    Renders and saves a Dual-Axis loss curve over the training duration.
    
    This function automatically aligns validation checkpoints with training 
    epochs for accurate visual comparison. It also highlights the 'Best' 
    validation epoch with a green annotation.
    
    Args:
        train_losses (List[float]): Training loss values per epoch.
        val_losses (List[float]): Validation loss values (calculated periodically).
        save_path (Union[Path, str]): Output path for the PNG chart.
        title (str): Title of the plot.
    """
    fig, ax = plt.subplots(1, 1, figsize=(12, 6))
    
    epochs = range(1, len(train_losses) + 1)
    ax.plot(epochs, train_losses, 'b-o', label='Train Loss', linewidth=2, markersize=4)
    
    if len(val_losses) > 0:
        # Calculate validation epochs based on the ratio of lengths
        # e.g., if train=100 and val=10, interval is 10, so val_epochs=[10, 20... 100]
        interval = len(train_losses) // len(val_losses)
        val_epochs = [i * interval for i in range(1, len(val_losses) + 1)]
        
        # Ensure lengths match for plotting
        v_limit = min(len(val_epochs), len(val_losses))
        ax.plot(val_epochs[:v_limit], val_losses[:v_limit], 'r-s', label='Val Loss', linewidth=2, markersize=4)
        
        # Mark best validation performance
        best_idx = np.argmin(val_losses)
        if best_idx < len(val_epochs):
            best_epoch = val_epochs[best_idx]
            ax.annotate(f'Best: {val_losses[best_idx]:.4f}\n(Epoch {best_epoch})',
                        xy=(best_epoch, val_losses[best_idx]),
                        xytext=(best_epoch + max(1, len(epochs) * 0.05), val_losses[best_idx]),
                        arrowprops=dict(arrowstyle='->', color='green'),
                        fontsize=10, color='green')
    
    ax.set_xlabel('Epochs', fontsize=12)
    ax.set_ylabel('Loss', fontsize=12)
    ax.set_title(title, fontsize=14)
    ax.legend()
    ax.grid(True, alpha=0.3)
    
    plt.tight_layout()
    plt.savefig(save_path, dpi=150, bbox_inches='tight')
    plt.close(fig)


def plot_metrics(psnr_list: List[float], ssim_list: List[float], 
                 save_path: Union[Path, str], title: str = 'Metrics Progress') -> None:
    """
    Renders and saves dual charts for PSNR and SSIM progression.
    
    Args:
        psnr_list (List[float]): History of PSNR values.
        ssim_list (List[float]): History of SSIM values.
        save_path (Union[Path, str]): File output path.
        title (str): Global title.
    """
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(14, 5))
    
    # Metrics are only recorded on validation epochs
    # We use the length of the metric lists and label them as checkpoints
    checkpoints = range(1, len(psnr_list) + 1)
    
    ax1.plot(checkpoints, psnr_list, 'g-o', linewidth=2, markersize=4)
    ax1.set_xlabel('Validation Checkpoint', fontsize=12)
    ax1.set_ylabel('PSNR (dB)', fontsize=12)
    ax1.set_title('PSNR over Checkpoints', fontsize=14)
    ax1.grid(True, alpha=0.3)
    
    ax2.plot(checkpoints, ssim_list, 'm-s', linewidth=2, markersize=4)
    ax2.set_xlabel('Validation Checkpoint', fontsize=12)
    ax2.set_ylabel('SSIM', fontsize=12)
    ax2.set_title('SSIM over Checkpoints', fontsize=14)
    ax2.grid(True, alpha=0.3)
    
    plt.suptitle(title, fontsize=15, y=1.02)
    plt.tight_layout()
    plt.savefig(save_path, dpi=150, bbox_inches='tight')
    plt.close(fig)


def plot_confusion_matrix(cm, classes, save_path, title='Character Confusion Matrix',
                          normalize=True):
    """
    Plot confusion matrix for character recognition.
    
    Args:
        cm: Confusion matrix (numpy array)
        classes: List of class labels
        save_path: Path to save figure
        title: Plot title
        normalize: Whether to normalize the CM
    """
    if normalize:
        row_sums = cm.sum(axis=1, keepdims=True)
        row_sums[row_sums == 0] = 1  # avoid div by zero
        cm_norm = cm.astype('float') / row_sums
    else:
        cm_norm = cm
    
    fig, ax = plt.subplots(figsize=(16, 14))
    
    im = ax.imshow(cm_norm, interpolation='nearest', cmap='Blues')
    ax.set_title(title, fontsize=16, pad=20)
    
    cbar = fig.colorbar(im, ax=ax, fraction=0.046, pad=0.04)
    cbar.set_label('Proportion' if normalize else 'Count', fontsize=12)
    
    tick_marks = np.arange(len(classes))
    ax.set_xticks(tick_marks)
    ax.set_xticklabels(classes, fontsize=8, rotation=45)
    ax.set_yticks(tick_marks)
    ax.set_yticklabels(classes, fontsize=8)
    
    ax.set_ylabel('Ground Truth', fontsize=14)
    ax.set_xlabel('Predicted', fontsize=14)
    
    # Add text annotations for non-zero cells
    thresh = cm_norm.max() / 2.0
    for i in range(len(classes)):
        for j in range(len(classes)):
            if cm[i, j] > 0:  # Only show non-zero
                ax.text(j, i, f'{cm[i, j]}',
                        ha='center', va='center', fontsize=6,
                        color='white' if cm_norm[i, j] > thresh else 'black')
    
    plt.tight_layout()
    plt.savefig(save_path, dpi=150, bbox_inches='tight')
    plt.close(fig)


def build_confusion_matrix(gt_texts, pred_texts):
    """
    Build per-character confusion matrix.
    
    Args:
        gt_texts: List of ground truth plate texts
        pred_texts: List of predicted plate texts
    
    Returns:
        cm: Confusion matrix (36x36 for A-Z + 0-9)
        classes: List of class labels
    """
    classes = [chr(i) for i in range(ord('A'), ord('Z') + 1)] + \
              [str(i) for i in range(10)]
    class_to_idx = {c: i for i, c in enumerate(classes)}
    
    n = len(classes)
    cm = np.zeros((n, n), dtype=int)
    
    for gt, pred in zip(gt_texts, pred_texts):
        min_len = min(len(gt), len(pred))
        for i in range(min_len):
            gt_c = gt[i].upper()
            pr_c = pred[i].upper()
            if gt_c in class_to_idx and pr_c in class_to_idx:
                cm[class_to_idx[gt_c], class_to_idx[pr_c]] += 1
    
    return cm, classes


