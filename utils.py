def calculate_psnr(img1: np.ndarray, img2: np.ndarray) -> float:
    """
    Calculates Peak Signal-to-Noise Ratio (PSNR) between two images.
    
    Args:
        img1 (np.ndarray): Reference (Ground Truth) image.
        img2 (np.ndarray): Recovered (Super-Resolved) image.
        
    Returns:
        float: The PSNR value in decibels (dB).
    """
    return peak_signal_noise_ratio(img1, img2)


def calculate_ssim(img1: np.ndarray, img2: np.ndarray) -> float:
    """
    Calculates Structural Similarity Index (SSIM) between two images.
    
    Uses a larger Gaussian weighting and accounts for RGB channels to ensure
    the metric matches human visual perception of alphanumeric clarity.
    
    Args:
        img1 (np.ndarray): Reference image.
        img2 (np.ndarray): Recovered image.
        
    Returns:
        float: SSIM index in range [0, 1].
    """
    return structural_similarity(
        img1, img2,
        gaussian_weights=True,
        channel_axis=2,
        sigma=1.5,
        use_sample_covariance=False,
        data_range=255.0
    )


def levenshtein(a: str, b: str) -> int:
    """
    Calculates the Levenshtein (Edit) distance between two strings.
    
    Used to quantify character-level prediction errors during training.
    
    Args:
        a (str): Source string (Ground Truth).
        b (str): Target string (SR Prediction).
        
    Returns:
        int: Minimum number of single-character edits required to transform a to b.
    """
    if not a:
        return len(b)
    if not b:
        return len(a)
    
    # Iterative DP version (avoids recursion stack overflow)
    m, n = len(a), len(b)
    dp = [[0] * (n + 1) for _ in range(m + 1)]
    
    for i in range(m + 1):
        dp[i][0] = i
    for j in range(n + 1):
        dp[0][j] = j
    
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            cost = 0 if a[i - 1] == b[j - 1] else 1
            dp[i][j] = min(
                dp[i - 1][j] + 1,      # deletion
                dp[i][j - 1] + 1,      # insertion
                dp[i - 1][j - 1] + cost  # substitution
            )
    
    return dp[m][n]


