def padding(img: np.ndarray, min_ratio: float, max_ratio: float, color: Tuple[int, int, int] = (127, 127, 127)) -> Tuple[np.ndarray, int, int]:
    """
    Pads an image with a solid color border to maintain an aspect ratio strictly within [min_ratio, max_ratio].
    
    This is highly useful for spatial transformers or CNN inputs, where severe distortion/stretching 
    might obliterate natural letter boundaries.
    
    Args:
        img (np.ndarray): The source image matrix.
        min_ratio (float): The minimum acceptable aspect ratio (width/height).
        max_ratio (float): The maximum acceptable aspect ratio (width/height).
        color (Tuple[int, int, int], optional): RGB background padding color. Defaults to (127, 127, 127).
        
    Returns:
        Tuple[np.ndarray, int, int]: The padded image, horizontal border width, vertical border height.
    """
    img_h, img_w = np.shape(img)[:2]
    border_w = 0
    border_h = 0
    ar = float(img_w) / img_h

    if ar >= min_ratio and ar <= max_ratio:
        return img, border_w, border_h

    if ar < min_ratio:
        while ar < min_ratio:
            border_w += 1
            ar = float(img_w + border_w) / (img_h + border_h)
    else:
        while ar > max_ratio:
            border_h += 1
            ar = float(img_w) / (img_h + border_h)

    border_w = border_w // 2
    border_h = border_h // 2

    img = cv2.copyMakeBorder(img, border_h, border_h, border_w, border_w,
                              cv2.BORDER_CONSTANT, value=color)
    return img, border_w, border_h


def rectify_image(img: np.ndarray, corners: Dict[str, List[float]], margin: int = 2) -> np.ndarray:
    """
    Rectify an obliquely captured license plate image using perspective transform algebra.
    
    Maps arbitrary quadrilateral coordinates (from object detection/landmarks) to a flat rectangular plane.
    
    Args:
        img (np.ndarray): The arbitrarily oriented input image array.
        corners (Dict[str, List[float]]): A dictionary denoting the four quadrilateral bounding box corners 
                                          in [X, Y] format: 'top-left', 'top-right', 'bottom-right', 'bottom-left'.
        margin (int, optional): The extra pixel border padded around the newly projected plate. Defaults to 2.
    
    Returns:
        np.ndarray: The flat, orthomosaic rectified image.
    """
    pts = np.array([
        corners['top-left'],
        corners['top-right'],
        corners['bottom-right'],
        corners['bottom-left']
    ], dtype='float32')
    
    (tl, tr, br, bl) = pts

    widthA = np.sqrt(((br[0] - bl[0]) ** 2) + ((br[1] - bl[1]) ** 2))
    widthB = np.sqrt(((tr[0] - tl[0]) ** 2) + ((tr[1] - tl[1]) ** 2))
    maxWidth = max(int(widthA), int(widthB))

    heightA = np.sqrt(((tr[0] - br[0]) ** 2) + ((tr[1] - br[1]) ** 2))
    heightB = np.sqrt(((tl[0] - bl[0]) ** 2) + ((tl[1] - bl[1]) ** 2))
    maxHeight = max(int(heightA), int(heightB))

    maxWidth += margin * 2
    maxHeight += margin * 2

    ww = maxWidth - 1 - margin
    hh = maxHeight - 1 - margin
    c1 = [margin, margin]
    c2 = [ww, margin]
    c3 = [ww, hh]
    c4 = [margin, hh]

    dst = np.array([c1, c2, c3, c4], dtype='float32')

    M = cv2.getPerspectiveTransform(pts, dst)
    warped = cv2.warpPerspective(img, M, (maxWidth, maxHeight))

    return warped


