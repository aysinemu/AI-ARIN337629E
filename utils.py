def save_comparison_grid(lr_tensors: torch.Tensor, sr_tensors: torch.Tensor, 
                         hr_tensors: torch.Tensor, gt_plates: List[str], 
                         ocr_module: Optional[Any], save_path: Union[Path, str], 
                         num_samples: int = 4) -> None:
    """
    Generates a high-quality visualization grid comparing quality levels.
    
    Row structure:
      1. LR (Bicubic Upsampled): Baseline low quality.
      2. SR (Model Output): Enhanced quality with OCR logic results.
      3. HR (Ground Truth): Target professional quality.
    
    Args:
        lr_tensors (torch.Tensor): Original LR inputs.
        sr_tensors (torch.Tensor): Model super-resolved outputs.
        hr_tensors (torch.Tensor): High-res targets.
        gt_plates (List[str]): Verified ground truth texts.
        ocr_module (Optional[Any]): The OCR module instance to run live prediction on SR.
        save_path (Union[Path, str]): Output PNG path.
        num_samples (int): Max columns in the grid.
    """
    num_samples = min(num_samples, lr_tensors.size(0))
    
    # Ensure save directory exists
    save_path = Path(save_path)
    save_path.parent.mkdir(parents=True, exist_ok=True)
    
    fig, axes = plt.subplots(3, num_samples, figsize=(5 * num_samples, 10))
    
    # Ensure axes is 2D even if num_samples=1
    if num_samples == 1:
        axes = np.expand_dims(axes, axis=1)
        
    for i in range(num_samples):
        # 1. LR (upscaled for comparison)
        lr_np = lr_tensors[i].cpu().permute(1, 2, 0).numpy()
        lr_np = np.clip(lr_np, 0, 1)
        lr_up = cv2.resize(lr_np, (hr_tensors.size(3), hr_tensors.size(2)), interpolation=cv2.INTER_LINEAR)
        axes[0, i].imshow(lr_up)
        axes[0, i].set_title(f"LR #{i}\nGT: '{gt_plates[i]}'")
        axes[0, i].axis('off')
        
        # 2. SR
        sr_np_torch = sr_tensors[i].cpu().detach()
        sr_np = sr_np_torch.permute(1, 2, 0).numpy()
        sr_np = np.clip(sr_np, 0, 1)
        
        # Predict on SR using OCR
        import torch
        from torchvision import transforms
        to_pil = transforms.ToPILImage()
        sr_pil_np = np.array(to_pil(sr_np_torch)).astype('uint8')
        pred = ocr_module.predict_plate(sr_pil_np) if ocr_module else "N/A"
        
        axes[1, i].imshow(sr_np)
        axes[1, i].set_title(f"SR #{i}\nOCR: '{pred}'")
        axes[1, i].axis('off')
        
        # 3. HR (Ground Truth)
        hr_np = hr_tensors[i].cpu().permute(1, 2, 0).numpy()
        hr_np = np.clip(hr_np, 0, 1)
        axes[2, i].imshow(hr_np)
        axes[2, i].set_title(f"HR (GT) #{i}")
        axes[2, i].axis('off')
        
    plt.tight_layout()
    plt.savefig(save_path, dpi=120, bbox_inches='tight')
    plt.close(fig)
