def setup_logging(save_dir: Union[Path, str], name: str = 'training') -> logging.Logger:
    """
    Bootstraps a professional dual-pipe logging system.
    
    Logs are simultaneously routed to:
      1. The system console (stdout) for real-time monitoring.
      2. A local filesystem `.log` file for long-term audit and debugging.
    
    Args:
        save_dir (Union[Path, str]): Directory where the log file will be archived.
        name (str): Semantic namespace for the logger instance.
    
    Returns:
        logging.Logger: The configured logger instance.
    """
    save_dir = Path(save_dir)
    save_dir.mkdir(parents=True, exist_ok=True)
    
    logger = logging.getLogger(name)
    logger.setLevel(logging.DEBUG)
    
    # Clear existing handlers
    logger.handlers.clear()
    
    # Console handler
    ch = logging.StreamHandler()
    ch.setLevel(logging.INFO)
    ch_fmt = logging.Formatter('[%(asctime)s] %(levelname)s: %(message)s', datefmt='%H:%M:%S')
    ch.setFormatter(ch_fmt)
    logger.addHandler(ch)
    
    # File handler
    fh = logging.FileHandler(save_dir / f'{name}.log', mode='a')
    fh.setLevel(logging.DEBUG)
    fh_fmt = logging.Formatter('[%(asctime)s] %(levelname)s: %(message)s', datefmt='%Y-%m-%d %H:%M:%S')
    fh.setFormatter(fh_fmt)
    logger.addHandler(fh)
    
    return logger


